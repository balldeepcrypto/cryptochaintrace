// ── Synchronous SHA-256 (pure-JS, no WebCrypto async) ─────────────────────────
// Used to produce the tamper-evident report hash without changing report
// generators from sync to async functions.
export function sha256Sync(str: string): string {
  const K = [
    0x428a2f98, 0x71374491, 0xb5c0fbcf, 0xe9b5dba5, 0x3956c25b, 0x59f111f1, 0x923f82a4, 0xab1c5ed5,
    0xd807aa98, 0x12835b01, 0x243185be, 0x550c7dc3, 0x72be5d74, 0x80deb1fe, 0x9bdc06a7, 0xc19bf174,
    0xe49b69c1, 0xefbe4786, 0x0fc19dc6, 0x240ca1cc, 0x2de92c6f, 0x4a7484aa, 0x5cb0a9dc, 0x76f988da,
    0x983e5152, 0xa831c66d, 0xb00327c8, 0xbf597fc7, 0xc6e00bf3, 0xd5a79147, 0x06ca6351, 0x14292967,
    0x27b70a85, 0x2e1b2138, 0x4d2c6dfc, 0x53380d13, 0x650a7354, 0x766a0abb, 0x81c2c92e, 0x92722c85,
    0xa2bfe8a1, 0xa81a664b, 0xc24b8b70, 0xc76c51a3, 0xd192e819, 0xd6990624, 0xf40e3585, 0x106aa070,
    0x19a4c116, 0x1e376c08, 0x2748774c, 0x34b0bcb5, 0x391c0cb3, 0x4ed8aa4a, 0x5b9cca4f, 0x682e6ff3,
    0x748f82ee, 0x78a5636f, 0x84c87814, 0x8cc70208, 0x90befffa, 0xa4506ceb, 0xbef9a3f7, 0xc67178f2,
  ];
  const rr  = (x: number, n: number): number => (x >>> n) | (x << (32 - n));
  const a32 = (...ns: number[]): number => ns.reduce((s, v) => s + v, 0) >>> 0;

  const bytes  = new TextEncoder().encode(str);
  const L      = bytes.length;
  const padLen = Math.ceil((L + 9) / 64) * 64;
  const buf    = new Uint8Array(padLen);
  buf.set(bytes);
  buf[L] = 0x80;
  const dv = new DataView(buf.buffer);
  dv.setUint32(padLen - 4, (L * 8) >>> 0, false);

  let h0 = 0x6a09e667, h1 = 0xbb67ae85, h2 = 0x3c6ef372, h3 = 0xa54ff53a;
  let h4 = 0x510e527f, h5 = 0x9b05688c, h6 = 0x1f83d9ab, h7 = 0x5be0cd19;

  for (let i = 0; i < padLen; i += 64) {
    const w: number[] = [];
    for (let j = 0; j < 16; j++) w[j] = dv.getUint32(i + j * 4, false);
    for (let j = 16; j < 64; j++) {
      const s0 = rr(w[j - 15], 7) ^ rr(w[j - 15], 18) ^ (w[j - 15] >>> 3);
      const s1 = rr(w[j - 2], 17) ^ rr(w[j - 2], 19)  ^ (w[j - 2]  >>> 10);
      w[j] = a32(w[j - 16], s0, w[j - 7], s1);
    }
    let a = h0, b = h1, c = h2, d = h3, e = h4, f = h5, g = h6, h = h7;
    for (let j = 0; j < 64; j++) {
      const S1  = rr(e, 6)  ^ rr(e, 11) ^ rr(e, 25);
      const ch  = (e & f)   ^ (~e & g);
      const t1  = a32(h, S1, ch, K[j], w[j]);
      const S0  = rr(a, 2)  ^ rr(a, 13) ^ rr(a, 22);
      const maj = (a & b)   ^ (a & c)   ^ (b & c);
      const t2  = a32(S0, maj);
      h = g; g = f; f = e; e = a32(d, t1);
      d = c; c = b; b = a; a = a32(t1, t2);
    }
    h0 = a32(h0, a); h1 = a32(h1, b); h2 = a32(h2, c); h3 = a32(h3, d);
    h4 = a32(h4, e); h5 = a32(h5, f); h6 = a32(h6, g); h7 = a32(h7, h);
  }
  return [h0, h1, h2, h3, h4, h5, h6, h7].map(n => n.toString(16).padStart(8, "0")).join("");
}

